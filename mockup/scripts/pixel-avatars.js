// --- SVG 렌더링 함수 ---
function renderPixelAvatar(memberId, size = 36) {
  const char = PIXEL_CHARACTERS[memberId];
  if (!char) return '';

  const grid = char.grid;
  const rows = grid.length;
  const cols = grid[0].length;
  const pixelSize = size / Math.max(rows, cols);

  let rects = '';
  for (let y = 0; y < rows; y++) {
    for (let x = 0; x < cols; x++) {
      const colorIdx = grid[y][x];
      if (colorIdx === 0) continue;
      const color = char.palette[colorIdx];
      if (!color) continue;
      rects += `<rect x="${x * pixelSize}" y="${y * pixelSize}" width="${pixelSize + 0.5}" height="${pixelSize + 0.5}" fill="${color}"/>`;
    }
  }

  return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 ${size} ${size}" width="${size}" height="${size}" style="image-rendering:pixelated">${rects}</svg>`;
}

// --- 전역 사용 가능하게 ---
window.renderPixelAvatar = renderPixelAvatar;
window.PIXEL_CHARACTERS = PIXEL_CHARACTERS;
